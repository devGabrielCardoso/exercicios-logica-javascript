// Exercício 8 - Área do triângulo
// Descrição: Algoritmo que solicita base e altura de um triângulo para calcular e mostrar o valor da área.
let base =  parseInt(prompt("Digite o valor da base de um triângulo"))
let altura = parseInt(prompt("Digite a altura de um triângulo"))
let area = (base*altura)/2
alert("A área do triângulo é: " + area)