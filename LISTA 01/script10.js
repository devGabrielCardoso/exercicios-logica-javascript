// Exercício 10 - Área do Losângulo
// Descrição: Solicita os valores da maior e menor diagonal de um losângulo para efetuar o cálculo da área.
let diagonalMaior = parseInt(prompt('Digite o valor da diagonal maior'))
let diagonalMenor = parseInt(prompt('Digite o valor da diagonal menor'))
let areaLosangulo = (diagonalMaior*diagonalMenor)/2
alert("A área do losângulo é: " + areaLosangulo)