// Exercício 7 - Área do retângulo 
// Descrição: Algoritmo que solicita base e altura de um retângulo para calcular a área.
let base = parseInt(prompt("Digite o valor da base de um retângulo"))
let altura = parseInt(prompt("Digite o valor da altura de um retângulo"))
let area = base*altura
alert("A área do retângulo é: " + area)