// Exercício 4 - Substração
// Descrição: Algoritmo que solicita dois valores e mostra o resultado da substração efetuada entre eles.
let numero1 = parseInt(prompt("Digite um número"))
let numero2 = parseInt(prompt("Digite outro número"))
