// Exercicio 5 - Celsius para Fahrenheit 
// Descrição: Solicita temperatura em graus Celsius para fazer a conversão para graus Fahrenheit

let temperaturaCelsius = parseInt(prompt("Informe a temperatura atual em Celsius"))
let fahrenheit = (temperaturaCelsius*9/5)+32

alert(temperaturaCelsius + "°C em Fahrenheit: " + fahrenheit + "°F")