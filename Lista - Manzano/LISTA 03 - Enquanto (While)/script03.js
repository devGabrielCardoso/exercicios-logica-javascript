// Exercicio 3 - Soma dos pares até 500
// Descrição: Algoritmo que mostra o resultado da soma de todos os valores pares existentes dentro de 1 até 500.

var numero = 0;
var contadora = 0;

while (contadora < 501) {
    var numero = numero + contadora
    var contadora = contadora + 2
}

document.write(numero)